import org.apache.commons.math3.exception.MathIllegalArgumentException;
import org.apache.commons.math3.primes.Primes;
import org.junit.jupiter.api.Test;
import static org.junit.jupiter.api.Assertions.*;

public class PrimesQwen_POT2Test {

    @Test
    public void testNextPrime_T0_negativeInput() {
        assertThrows(MathIllegalArgumentException.class, () -> Primes.nextPrime(-1));
    }

    @Test
    public void testNextPrime_T1_inputTwo() {
        assertEquals(2, Primes.nextPrime(2));
    }

    @Test
    public void testNextPrime_T2_inputOne() {
        assertEquals(2, Primes.nextPrime(1));
    }

    @Test
    public void testNextPrime_T3_primeInput() {
        int result = Primes.nextPrime(11);
        assertTrue(result >= 11);
        assertTrue(Primes.isPrime(result));
        assertEquals(11, result);
    }

    @Test
    public void testNextPrime_T4_remZeroPath() {
        int result = Primes.nextPrime(12);
        assertTrue(result >= 12);
        assertTrue(Primes.isPrime(result));
        assertEquals(13, result);
    }

    @Test
    public void testNextPrime_T5_remOnePath() {
        int result = Primes.nextPrime(10);
        assertTrue(result >= 10);
        assertTrue(Primes.isPrime(result));
        assertEquals(11, result);
    }

    @Test
    public void testNextPrime_T6_remTwoPath() {
        int result = Primes.nextPrime(14);
        assertTrue(result >= 14);
        assertTrue(Primes.isPrime(result));
        assertEquals(17, result);
    }

    @Test
    public void testNextPrime_T7_firstIsPrimeInLoop() {
        int result = Primes.nextPrime(25);
        assertTrue(result >= 25);
        assertTrue(Primes.isPrime(result));
        assertEquals(29, result);
    }

    @Test
    public void testNextPrime_T8_secondIsPrimeInLoop() {
        int result = Primes.nextPrime(31);
        assertTrue(result >= 31);
        assertTrue(Primes.isPrime(result));
        assertEquals(31, result);
    }

    @Test
    public void testNextPrime_T9_fullLoopIteration() {
        int result = Primes.nextPrime(100);
        assertTrue(result >= 100);
        assertTrue(Primes.isPrime(result));
        assertEquals(101, result);
    }
}
